    /**
     * 清洗輸入值（移轉與儲存時呼叫）
     *
     * - 修復 NinjaTeam 2.3.6 產生的雙重網址。
     * - LINE 完整網址 → 抽出純 ID（lin.ee 短網址除外，原樣保留）。
     * - 其他值僅 trim。
     */
    public static function normalize_value( string $key, string $value ): string {
        $value = trim( $value );
        if ( '' === $value ) {
            return '';
        }

        // 修復雙重網址：取最後一個 http(s):// 之後的完整網址。
        if ( preg_match_all( '#https?://#i', $value ) > 1 ) {
            $pos = strripos( $value, 'https://' );
            if ( false === $pos ) {
                $pos = strripos( $value, 'http://' );
            }
            if ( false !== $pos ) {
                $value = substr( $value, $pos );
            }
        }

        if ( 'line' === $key ) {
            // line.me 網址 → 抽出純 ID（@xxx）；官方短網址 lin.ee 原樣保留。
            if ( preg_match( '#line\.me/(?:R/)?(?:ti/p|oaMessage)/(@?[^/?\#\s]+)#i', $value, $m ) ) {
                return rawurldecode( $m[1] );
            }
        }

        return $value;
    }

    /**
     * 組出前端輸出用的完整連結（value 可為完整網址或純 ID，兩者相融）
     */
    public static function build_url( string $key, string $value ): string {
        $value = self::normalize_value( $key, $value );
        if ( '' === $value ) {
            return '';
        }

        // 已帶 scheme（https: / tel: / mailto: / viber: / weixin: 等）→ 原樣使用。
        if ( preg_match( '#^[a-z][a-z0-9+.\-]*:#i', $value ) ) {
            // LINE 網域強制 https（http 版在 App 內會失效）。
            if ( false !== stripos( $value, 'line.me' ) || false !== stripos( $value, 'lin.ee' ) ) {
                $value = preg_replace( '#^http://#i', 'https://', $value );
            }
            return $value;
        }

        // lin.ee 純短碼網址（無 scheme）。
        if ( 0 === stripos( $value, 'lin.ee/' ) ) {
            return 'https://' . $value;
        }

        $prefixes = self::prefixes();
        if ( ! isset( $prefixes[ $key ] ) ) {
            // custom：無 scheme 一律補 https://。
            return 'https://' . ltrim( $value, '/' );
        }

        // 號碼類 App 清掉空白與連字號。
        if ( in_array( $key, [ 'whatsapp', 'phone', 'viber' ], true ) ) {
            $value = preg_replace( '/[\s\-()]/', '', $value );
        }
        if ( 'whatsapp' === $key ) {
            $value = ltrim( $value, '+' ); // wa.me 不吃 +。
        }

        // LINE ID 需 URL encode（@ 保留原樣即可，line.me 接受）。
        if ( 'line' === $key ) {
            $value = rawurlencode( $value );
            $value = str_replace( '%40', '@', $value );
        }

        return $prefixes[ $key ] . $value;
    }

